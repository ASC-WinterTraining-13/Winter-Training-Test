// *************************** 例程常见问题说明 ***************************
// 遇到问题时请按照以下问题检查列表检查
// 
// 问题1：蓝牙无法连接或串口没有数据
//      检查HC-04模块的电源是否正确连接（VCC和GND）
//      检查RX/TX线是否正确连接（单片机的TX接模块RX，单片机的RX接模块TX）
//      检查EN引脚是否接高电平（正常模式）或悬空
//      使用AT命令检查模块波特率是否与程序设置一致
//      查看核心板LED灯状态 如果快速闪烁证明初始化失败 检查接线
// 
// 问题2：串口数据乱码
//      检查波特率是否匹配，HC-04默认波特率为9600
//      检查模块AT命令设置的波特率是否与程序初始化波特率一致
// 
// 问题3：发送数据没有收到回复
//      检查蓝牙是否已连接（可以通过STATE引脚或hc_04_get_state()函数判断）
//      调试看看是否读取到数据 data_len 是否一直为0
//      检查模块是否工作正常，尝试使用AT命令测试模块
// 
// 问题4：连接不稳定
//      确保蓝牙模块与手机/PC的距离在有效范围内（通常10米内）
//      检查是否有其他蓝牙设备干扰
//      尝试使用不同的蓝牙串口APP进行测试