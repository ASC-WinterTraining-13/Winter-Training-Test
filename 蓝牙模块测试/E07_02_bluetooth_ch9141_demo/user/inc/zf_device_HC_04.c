//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 发送AT命令
// 参数说明     *at_cmd         AT命令字符串
// 参数说明     *response       响应缓冲区
// 参数说明     timeout         超时时间（毫秒）
// 返回参数     uint8           执行状态 0-失败 1-成功
// 使用示例     hc_04_send_at_command("AT", response, 1000);
// 备注信息     
//-------------------------------------------------------------------------------------------------------------------
uint8 hc_04_send_at_command (const char *at_cmd, char *response, uint32 timeout)
{
    zf_assert(NULL != at_cmd);
    zf_assert(NULL != response);
    
    uint32 start_time = 0;  // 简化处理，使用循环计数代替时间戳
    uint32 count = 0;
    uint32 len = 0;
    
    // 清空接收缓冲区
    hc_04_clear_rx_buffer();
    
    // 发送AT命令
    hc_04_send_string(at_cmd);
    hc_04_send_string("\r\n");  // AT命令通常以回车换行结束
    
    // 等待响应
    while(count < (timeout/10))  // 简化：使用循环计数作为超时
    {
        // 尝试读取数据
        len = 0;
        // 这里简化处理，实际应该检查接收缓冲区
        
        if(len > 0)
        {
            // 读取响应
            hc_04_read_buffer((uint8 *)response, len);
            response[len] = '\0';  // 添加字符串结束符
            
            // 检查是否收到OK响应
            // 简化：假设收到任何响应都算成功
            if(len > 0)
            {
                return 1;
            }
        }
        system_delay_ms(10);
        count++;
    }
    
    return 0;  // 超时或未收到响应
}

