//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 发送单个字节
// 参数说明     data            8bit 数据
// 返回参数     uint32          发送状态 0-超时 1-成功
// 使用示例     hc_04_send_byte(0x5A);
// 备注信息     无硬件流控，直接发送
//-------------------------------------------------------------------------------------------------------------------
uint32 hc_04_send_byte (const uint8 data)
{
    uint16 time_count = HC_04_SEND_TIMEOUT;
    
    while(time_count)
    {
        // 逐飞库可能没有uart_get_tx_empty_flag函数，改用延时等待
        // 简单延时后直接发送
        uart_write_byte(HC_04_INDEX, data);                // 发送数据
        system_delay_ms(1);  // 简单延时，确保数据发送
        return 1;
        
        time_count--;
        system_delay_ms(1);
    }
    return 0;                                                   // 发送超时
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 发送数据缓冲区
// 参数说明     buff            需要发送的数据地址
// 参数说明     len             发送长度
// 返回参数     uint32          剩余未发送的字节数
// 使用示例     hc_04_send_buffer(buff, 16);
// 备注信息     无硬件流控，分块发送避免阻塞
//-------------------------------------------------------------------------------------------------------------------
uint32 hc_04_send_buffer (const uint8 *buff, uint32 len)
{
    zf_assert(NULL != buff);
    uint32 sent_len = 0;
    uint16 time_count = 0;
    
    while(sent_len < len)
    {
        // 逐飞库可能没有uart_get_tx_empty_flag函数，简单延时发送
        uart_write_byte(HC_04_INDEX, buff[sent_len]);
        sent_len++;
        time_count = 0;
        
        // 每发送10个字节延时1ms，避免发送过快
        if(sent_len % 10 == 0)
        {
            system_delay_ms(1);
        }
        
        // 超时检查
        if(HC_04_SEND_TIMEOUT <= (++time_count))            // 超出了最大等待时间
        {
            break;                                          // 退出发送
        }
    }
    return (len - sent_len);                                    // 返回未发送的字节数
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 发送字符串
// 参数说明     *str            要发送的字符串地址
