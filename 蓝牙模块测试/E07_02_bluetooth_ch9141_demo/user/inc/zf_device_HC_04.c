{
    uint8 data = 0;
    uint32 len = 1;
    
    // 逐飞库可能没有fifo_get_used_size函数，改用fifo_read_buffer检查
    // 尝试读取一个字节
    fifo_read_buffer(&hc_04_fifo, &data, &len, FIFO_READ_ONLY);
    
    return data;
}

