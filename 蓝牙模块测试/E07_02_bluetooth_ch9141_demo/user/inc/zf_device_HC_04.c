// 使用示例     hc_04_set_baud_rate(115200);
// 备注信息     设置成功后需要重新初始化串口
//-------------------------------------------------------------------------------------------------------------------
uint8 hc_04_set_baud_rate (uint32 baud_rate)
{
    char at_cmd[64];
    char response[128];
    
    // 修正格式化字符串，使用%u而不是%lu
    sprintf(at_cmd, "AT+UART=%u,0,0", baud_rate);
    
    if(hc_04_send_at_command(at_cmd, response, 1000))
    {
        hc_04_current_baud = baud_rate;
        return 1;
    }
    
    return 0;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 初始化
// 参数说明     baud_rate       波特率（可选，默认为HC_04_BAUD_RATE）
// 返回参数     uint8           初始化状态 0-成功 1-失败
// 使用示例     hc_04_init(9600); 或 hc_04_init(HC_04_BAUD_RATE);
// 备注信息     
//-------------------------------------------------------------------------------------------------------------------
uint8 hc_04_init (uint32 baud_rate)
{
    uint8 return_state = 0;
    
    if(baud_rate == 0)
    {
        baud_rate = HC_04_BAUD_RATE;
    }
    
    hc_04_current_baud = baud_rate;
    
    // 注册设备类型和回调函数
    // 注意：如果zf_device_type.h中没有BLUETOOTH_HC_04，可以使用其他蓝牙类型
    // 或者修改zf_device_type.h添加BLUETOOTH_HC_04
    set_wireless_type(BLUETOOTH_CH9141, hc_04_uart_callback);  // 暂时使用CH9141类型
    
    // 初始化FIFO缓冲区
    fifo_init(&hc_04_fifo, FIFO_DATA_8BIT, hc_04_buffer, HC_04_BUFFER_SIZE);
    
#ifdef HC_04_STATE_PIN
    // 初始化状态引脚（如果有）
    gpio_init(HC_04_STATE_PIN, GPI, 0, GPI_PULL_UP);
#endif
    
    // 初始化串口
    // 注意：根据逐飞库的uart_init函数参数顺序调整
    // uart_init参数：串口号，波特率，TX引脚，RX引脚
    uart_init(HC_04_INDEX, hc_04_current_baud, HC_04_TX_PIN, HC_04_RX_PIN);
    uart_rx_interrupt(HC_04_INDEX, 1);
    
    return return_state;
}

