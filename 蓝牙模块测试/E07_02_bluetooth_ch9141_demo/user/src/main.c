// *************************** 例程硬件连接说明 ***************************
// 接入 HC-04 蓝牙模块
//      模块管脚            单片机管脚
//      RX                  查看 zf_device_HC_04.h 中 HC_04_TX_PINx 宏定义 默认 B10
//      TX                  查看 zf_device_HC_04.h 中 HC_04_RX_PINx 宏定义 默认 B11
//      EN                  接高电平或悬空（进入AT模式时需接低电平）
//      STATE               状态指示引脚（可选，连接 HC_04_STATE_PIN 定义引脚）
//      GND                 核心板电源地 GND
//      VCC                 核心板 3.3V 或 5V 电源（根据模块版本）
// 
// 注意：HC-04 模块没有硬件流控(RTS/CTS)，因此发送数据时不需要等待流控信号



// *************************** 例程测试说明 ***************************
// 1.核心板烧录完成本例程
// 
// 2.如果使用主板测试 在断电情况下 将核心板插入主板 HC-04模块插入主板无线模块接口
// 
// 3.如果使用核心板和模块测试 在断电情况下 将核心板与无线模块连接
// 
// 4.通过手机或PC连接HC-04蓝牙模块，打开串口助手（如蓝牙串口APP）并连接
//   连接时可能需要输入配对码（默认"1234"或"0000"），波特率查看 zf_device_HC_04.h 中 
//   HC_04_BAUD_RATE 宏定义，默认9600
// 
// 5.单片机上电复位后 可以在蓝牙串口助手上看到如下串口信息：
//      SEEKFREE HC-04 demo.
// 
// 6.蓝牙串口助手发送数据会收到回复消息，并显示接收到的数据长度
// 
// 如果发现现象与说明严重不符 请参照本文件最下方 例程常见问题说明 进行排查


// **************************** 代码区域 ****************************
#define LED1                    (H2 )

uint8 data_buffer[32];
uint8 data_len;

int main (void)
{
    clock_init(SYSTEM_CLOCK_120M);                                              // 初始化芯片时钟 工作频率为 120MHz
    debug_init();                                                               // 初始化默认 Debug UART

    // 此处编写用户代码 例如外设初始化代码等
    uint8 count = 0;

    gpio_init(LED1, GPO, GPIO_HIGH, GPO_PUSH_PULL);                             // 初始化 LED1 输出 默认高电平 推挽输出模式
    if(hc_04_init(HC_04_BAUD_RATE))                                             // 判断是否通过初始化
    {
        while(1)                                                                // 初始化失败就在这进入死循环
        {
            gpio_toggle_level(LED1);                                            // 翻转 LED 引脚输出电平 控制 LED 亮灭
            system_delay_ms(100);                                               // 短延时快速闪灯表示异常
        }
    }
    hc_04_send_byte('\r');
    hc_04_send_byte('\n');
    hc_04_send_string("SEEKFREE HC-04 demo.\r\n");                              // 初始化正常 输出测试信息
    // 此处编写用户代码 例如外设初始化代码等

    while(1)
    {
        // 此处编写需要循环执行的代码
        data_len = hc_04_read_buffer(data_buffer, 32);                          // 查看是否有消息 默认缓冲区是 HC_04_BUFFER_SIZE 总共 128 字节
        if(0 != data_len)                                                       // 收到了消息 读取函数会返回实际读取到的数据个数
        {
            hc_04_send_buffer(data_buffer, data_len);                           // 将收到的消息发送回去
            memset(data_buffer, 0, 32);
            func_uint_to_str((char *)data_buffer, data_len);
            hc_04_send_string("\r\ndata len:");                                 // 显示实际收到的数据信息
            hc_04_send_buffer(data_buffer, strlen((const char *)data_buffer));  // 显示收到的数据个数
            hc_04_send_string(".\r\n");
        }
        
        // 检查蓝牙连接状态（可选）
        if(hc_04_get_state() == HC_04_STATE_CONNECTED)
        {
            // 已连接，LED正常闪烁
            if(10 < count ++)
            {
                count = 0;
                gpio_toggle_level(LED1);                                        // 翻转 LED 引脚输出电平 控制 LED 亮灭
            }
        }
        else
        {
            // 未连接，LED快速闪烁
            if(5 < count ++)
            {
                count = 0;
                gpio_toggle_level(LED1);                                        // 翻转 LED 引脚输出电平 控制 LED 亮灭
            }
        }
        system_delay_ms(50);
        // 此处编写需要循环执行的代码
    }
}
