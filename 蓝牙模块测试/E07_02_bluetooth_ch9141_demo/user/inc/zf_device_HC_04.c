//-------------------------------------------------------------------------------------------------------------------
// 函数简介     HC-04 蓝牙模块 读取数据到缓冲区
// 参数说明     buff            存储的数据地址
// 参数说明     len             期望读取的长度
// 返回参数     uint32          实际读取字节数
// 使用示例     hc_04_read_buffer(buff, 16);
// 备注信息     
//-------------------------------------------------------------------------------------------------------------------
uint32 hc_04_read_buffer (uint8 *buff, uint32 len)
{
    zf_assert(NULL != buff);
    uint32 data_len = len;
    fifo_read_buffer(&hc_04_fifo, buff, &data_len, FIFO_READ_AND_CLEAN);
    return data_len;
}

