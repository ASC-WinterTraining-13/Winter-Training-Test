// 使用示例     hc_04_send_string("Hello HC-04");
// 备注信息     
//-------------------------------------------------------------------------------------------------------------------
uint32 hc_04_send_string (const char *str)
{
    zf_assert(NULL != str);
    return hc_04_send_buffer((const uint8 *)str, strlen(str));
}

